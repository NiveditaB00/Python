"""In Python, the ^ operator is the bitwise XOR (exclusive OR) operator. It operates on the binary representations of integers and performs the XOR operation on each corresponding bit.

How the Bitwise XOR Works:
Bitwise XOR Operation:
It compares corresponding bits of two numbers.
The result bit is 1 if the bits are different (i.e., one is 1 and the other is 0).
The result bit is 0 if the bits are the same (i.e., both are 0 or both are 1)

EX:
a = 5  # Binary: 0101
b = 3  # Binary: 0011
result = a ^ b  # Binary: 0110 (6 in decimal)
print(result)  # Output: 6
"""