"""Explain the difference between the = operator and the == operator in Python.
= Operator (Assignment Operator):
The = operator is used to assign a value to a variable.
It takes the value on the right-hand side and stores it in the variable on the left-hand side.
EX:
x = 10
name = "Alice"

== Operator (Equality Operator):
The == operator is used to compare two values for equality.
It evaluates to True if the values on both sides are equal and False otherwise.
EX:
x = 10
y = 20
print(x == y)  # Output: False
print(x == 10)  # Output: True
name = "Alice"
print(name == "Alice")  # Output: True
"""